/*
This generator is based on the work of a few papers.

The neutron spectrum comes from Figure 5 of Marsh, Thomas, and Burke, "High
Resolution Measurements of Neutron Energy Spectra from Am-Be and Am-B Neutron
Sources", Nuclear Instruments and Methods A 366 (1995) p.340-8. The spectrum was
digitized and normalized to create a probability density function. This PDF was
converted to a CDF to create the lookup tables present in the constructor. A
random number is selected, and a linear interpolation performed between energy
bins.

The gamma energies were taken from the NuDat data base at
http://www.nndc.bnl.gov/nudat2/getdataset.jsp?nucleus=12C&unc=nds and the
decay structure came from the Isotope Explorer at http://ie.lbl.gov/ensdf 

Knowing which gammas to produce in coincidence with which neutron energies came
from Geiger and Zwan, "Radioactive Neutron Source Spectra from 9Be(a,n) Cross
Section Data", Nuclear Instruments and Methods 131 (1975) p.315-21. In this
paper, they show that neutrons up to ~0.5 MeV are not accompanied by a gamma
(they are breakup neutrons). When a neutron between 0.5 and 1.9 MeV is created,
the resulting C12 nucleus is in the 7654 keV energy level, and the neutron
should be accompanied by the appropriate gamma cascade. When the emitted neutron
has energy between 1.9 MeV and 6.0 MeV, the C12 nucleus is in the 4439 keV
state. Higher-energy neutrons result in ground-state C12 nuclei, so there are
no accompanying gammas. We would like to have GEANT4 produce the appropriate
gammas by creating an excited C12 ion in the appropriate energy state.

Unfortunately, the excited C12 nucleus does not decay properly via gamma
emission. To roll our own gamma generator, we have to take into account the
angular correlations. The 7654 keV energy level of C12 is a 0+ state, the 4439
keV level is 2+, and the ground state is again 0+. The two gammas must have an
angular momentum of two with no parity change, i.e., an electric quadrupole (E2)
transition. Thus the angular relationship between the two gammas goes as
P_2(cos theta)^2, where P_2 is the second Legendre polynomial. We assume the C12
nuclei are not polarized, so a random polarization angle is generated, then the
two gammas are generated relative to that.

*/

